def calculate_completed_age(dob: date, policy_start: date) -> int:
    age = policy_start.year - dob.year
    if (policy_start.month, policy_start.day) < (dob.month, dob.day):
        age -= 1
    return age

def get_age_band_icici(age: int) -> str:
    if age <= 5: return "0_5"
    elif age <= 10: return "6_10"
    elif age <= 15: return "11_15"
    elif age <= 20: return "16_20"
    elif age <= 25: return "21_25"
    elif age <= 30: return "26_30"
    elif age <= 35: return "31_35"
    elif age <= 40: return "36_40"
    elif age <= 45: return "41_45"
    elif age <= 50: return "46_50"
    elif age <= 55: return "51_55"
    elif age <= 60: return "56_60"
    elif age <= 65: return "61_65"
    elif age <= 70: return "66_70"
    elif age <= 75: return "71_75"
    elif age <= 80: return "76_80"
    else: return "80_"

def get_icici_family_type(num_adults: int, num_children: int) -> str:
    if num_adults == 1 and num_children == 0:
        return "Individual"
    elif num_adults == 2 and num_children == 0:
        return "2_Adults"
    elif num_adults == 2 and num_children == 1:
        return "2_Adults_1_Child"
    elif num_adults == 2 and num_children == 2:
        return "2_Adults_2_Children"
    elif num_adults == 2 and num_children == 3:
        return "2_Adults_3_Children"
    elif num_adults == 1 and num_children == 1:
        return "1_Adult_1_Child"
    elif num_adults == 1 and num_children == 2:
        return "1_Adult_2_Children"
    elif num_adults == 1 and num_children == 3:
        return "1_Adult_3_Children"
    else:
        raise ValueError(f"Unsupported family combination: {num_adults} adults, {num_children} children")

def get_icici_premium(family_type: str, age_band: str, si: str) -> int:
    try:
        return ICICI_BASE[family_type][age_band][si]
    except KeyError:
        raise ValueError(f"ICICI: No rate for {family_type}, {age_band}, {si}")

def get_tata_premium(age: int, zone: str, si: str) -> int:
    zones = {"Zone_A": TATA_ZONE_A, "Zone_B": TATA_ZONE_B, "Zone_C": TATA_ZONE_C}
    rates = zones.get(zone)
    if rates is None:
        raise ValueError(f"Invalid Tata zone: {zone}")
    if age in rates:
        return rates[age][si]
    if age > 90:
        return rates[90][si]
    if age < 18:
        return rates[18][si]
    closest = min(rates.keys(), key=lambda x: abs(x - age))
    return rates[closest][si]

def apply_discounts(base: float, discounts: List[float]) -> float:
    for d in discounts:
        base *= (1 - d / 100)
    return base

