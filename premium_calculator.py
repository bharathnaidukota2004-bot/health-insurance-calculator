def get_tata_floater_discount(num_adults: int, num_children: int) -> int:
    total = num_adults + num_children
    if total == 1:
        return 0
    elif total == 2:
        return 22
    elif total == 3:
        return 28 if num_children >= 1 else 22
    else:
        if num_children >= 2:
            return 32
        elif num_children >= 1:
            return 28
        else:
            return 22

