def calculate_icici_premium(
    members: List[Tuple[str, date]],
    policy_start: date,
    zone: str,
    sum_insured: str,
    tenure: int,
    professional_discount: bool,
    young_family_discount: bool,
) -> Dict[str, Any]:
    ages = [calculate_completed_age(dob, policy_start) for _, dob in members]
    eldest_age = max(ages)
    age_band = get_age_band_icici(eldest_age)
    num_adults = sum(1 for age in ages if age >= 18)
    num_children = len(members) - num_adults
    family_type = get_icici_family_type(num_adults, num_children)
    
    original_base = get_icici_premium(family_type, age_band, sum_insured)
    discounted_base = original_base * ICICI_ZONE_MULTIPLIER.get(zone, 1.0)
    lt_discount = ICICI_LONG_TERM_DISCOUNT.get(tenure, 0)
    discounts = [lt_discount]
    if professional_discount:
        discounts.append(PROFESSIONAL_DISCOUNT)
    if young_family_discount:
        discounts.append(YOUNG_FAMILY_DISCOUNT)
    discounted_base = apply_discounts(discounted_base, discounts)
    gst = discounted_base * GST_RATE / 100
    total = discounted_base + gst
    return {
        "sum_insured": sum_insured,
        "eldest_age": eldest_age,
        "family_type": family_type,
        "original_base": original_base,
        "zone_multiplier": ICICI_ZONE_MULTIPLIER.get(zone, 1.0),
        "discounts_applied": discounts,
        "premium_excluding_gst": round(discounted_base, 2),
        "gst_amount": round(gst, 2),
        "premium_including_gst": round(total, 2),
    }

def calculate_tata_premium(
    members: List[Tuple[str, date]],
    policy_start: date,
    zone: str,
    sum_insured: str,
    tenure: int,
    professional_discount: bool,
    young_family_discount: bool,
) -> Dict[str, Any]:
    ages = [calculate_completed_age(dob, policy_start) for _, dob in members]
    num_adults = sum(1 for age in ages if age >= 18)
    num_children = len(members) - num_adults
    
    individual_premiums = []
    for name, dob in members:
        age = calculate_completed_age(dob, policy_start)
        prem = get_tata_premium(age, zone, sum_insured)
        individual_premiums.append((name, age, prem))
    original_total = sum(p for _, _, p in individual_premiums)
    
    ff_discount = get_tata_floater_discount(num_adults, num_children)
    discounted_base = original_total * (1 - ff_discount / 100)
    
    lt_discount = TATA_LONG_TERM_DISCOUNT.get(tenure, 0)
    discounts = [lt_discount]
    if professional_discount:
        discounts.append(PROFESSIONAL_DISCOUNT)
    if young_family_discount:
        discounts.append(YOUNG_FAMILY_DISCOUNT)
    discounted_base = apply_discounts(discounted_base, discounts)
    
    gst = discounted_base * GST_RATE / 100
    total = discounted_base + gst
    return {
        "sum_insured": sum_insured,
        "members": individual_premiums,
        "original_total": original_total,
        "family_floater_discount": ff_discount,
        "discounts_applied": discounts,
        "premium_excluding_gst": round(discounted_base, 2),
        "gst_amount": round(gst, 2),
        "premium_including_gst": round(total, 2),
    }

