def get_all_premiums(
    insurer: str,
    members: List[Tuple[str, date]],
    policy_start: date,
    zone: str,
    si_list: List[str],
    tenure: int,
    professional_discount: bool,
    young_family_discount: bool,
) -> Dict[str, Dict]:
    results = {}
    for si in si_list:
        try:
            if insurer == "ICICI":
                res = calculate_icici_premium(members, policy_start, zone, si, tenure,
                                              professional_discount, young_family_discount)
            elif insurer == "Tata":
                res = calculate_tata_premium(members, policy_start, zone, si, tenure,
                                             professional_discount, young_family_discount)
            else:
                raise ValueError(f"Unknown insurer: {insurer}")
            results[si] = res
        except Exception as e:
            results[si] = {"error": str(e)}
    return results

